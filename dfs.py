    expanded_nodes = 0
    max_frontier_size = 1
    solution_steps = 0
    MAX_DEPTH = 1000

    while frontier:
        node = frontier.pop()
        node_action = actions.pop()
        state = node[-1]
        playerPos, boxPos = state

        expanded_nodes += 1
        max_frontier_size = max(max_frontier_size, len(frontier))

        if isEndState(boxPos):
            solution_steps = len(node_action) - 1
            print("=== DFS Solution Found ===")
            print("Path:", ''.join(node_action[1:]))
            break

        if state not in exploredSet:
            exploredSet.add(state)
            if len(node) > MAX_DEPTH:
                continue

            for action in legalActions(playerPos, boxPos):
                newPosPlayer, newPosBox = updateState(playerPos, boxPos, action)
                if isFailed(newPosBox):
                    continue
                newState = (newPosPlayer, tuple(newPosBox))
                if newState not in exploredSet:
                    frontier.append(node + [newState])
                    actions.append(node_action + [action[-1]])

    # --- Performance statistics ---
    end_time = time.time()
    memo_info = process.memory_info().rss / (1024 * 1024) - itemMemory
    runtime = end_time - start_time

    print("\n=== DFS Statistics ===")
    print(f"Expanded nodes   : {expanded_nodes}")
    print(f"Explored states  : {len(exploredSet)}")
    print(f"Max frontier size: {max_frontier_size}")
    print(f"Solution steps   : {solution_steps if solution_steps else 'No solution'}")
    print(f"Memory used (MB) : {memo_info:.3f}")
    print(f"Time elapsed (s) : {runtime:.3f}")


